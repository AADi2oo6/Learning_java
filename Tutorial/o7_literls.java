class o7_literls{
    public static void main(String a[]){

        //literls
        int fee = 1_92_945;
        System.out.println(fee);
        fee++;
        System.out.println(fee);

        //in java increment works on charachers as well
        char alphabet = 'A';
        alphabet++;
        System.out.println(alphabet);

        char digit = '9';
        digit--;
        System.out.println(digit);

    }
}
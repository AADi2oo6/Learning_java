// primitive is a Data type which can't be break further

public class o2_PremitiveDataTypes{
    public static void main(String[] args){
        System.out.println("Lets see the different Premitive data types" );

        // 1. int
        int x = 12; 

        //2. String : Note String is not a premitive data type 
        String Name = "Aditya Sharma"; // we can break it into parts therefor Not a premitive;

        // 3. Char 
        char letter = 'A'; // note Char starts with single quotes

        // 4. float
        float y = 123.22f;

        // 5. double 
        double largeDecimalNumber = 1232.123123123;

        // 6. long
        long largeIntNumber = 123124234324523532L;

        // 7. boolean
        boolean bol = true;
        System.out.println(x+Name+letter+y+largeDecimalNumber+largeIntNumber+bol);



        
    }
}